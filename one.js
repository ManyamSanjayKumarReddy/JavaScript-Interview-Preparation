// Operators
let x = 10;
let y = 4;

y += 11;

console.log(y); // Outputs 15
console.log(5 % 2); // Outputs the remainder of 5 divided by 2, which is 1
console.log(5 == 2); // Outputs 'false' because 5 is not equal to 2
console.log(5 === '5'); // Outputs 'false' because strict equality is not satisfied
console.log(true && true); // Outputs 'true' because both operands are 'true'

let userlogin = false;
let userdetails = true;

// Using logical AND operator (&&)
console.log(userlogin && userdetails); // Outputs 'false' because one operand is 'false'

// Using logical OR operator (||)
console.log(userlogin || userdetails); // Outputs 'true' because one operand is 'true'
